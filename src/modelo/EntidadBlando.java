
package modelo;

/**
 *
 * @author moisesvelasco
 */
public class EntidadBlando {
    int idBlando;
    String nombreBlando;

    public EntidadBlando() {
    }

    public EntidadBlando(int idBlando, String nombreBlando) {
        this.idBlando = idBlando;
        this.nombreBlando = nombreBlando;
    }

    public int getIdBlando() {
        return idBlando;
    }

    public void setIdBlando(int idBlando) {
        this.idBlando = idBlando;
    }

    public String getNombreBlando() {
        return nombreBlando;
    }

    public void setNombreBlando(String nombreBlando) {
        this.nombreBlando = nombreBlando;
    }
    
    
}

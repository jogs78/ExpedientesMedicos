    void agregarMedico(){
        try {
        int idPaciente = Integer.parseInt(txtId.getText());
        String nom=txtnomMedico.getText();
        String cont=txtContac.getText();
        String medica=txtMedicamento.getText();
        String hospita=txtHopitalizado.getText();
        String no="NO";
        String Tmedico="MEDICO";
        String Tdental="DENTAL";
        
        Object[] ob = new Object[6];
        
        if(checMedico.isSelected()==true){ 
            ob[0]=idPaciente;
            ob[1]=Tmedico;
            ob[2]=nom;
            ob[3]=cont;
           if(tomaMsi.isSelected()==true){  
               ob[4]=medica;
               if(hospSi.isSelected()==true){
                   ob[5]=hospita;
                   dmD.add(ob);
               }
               if(hospNo.isSelected()==true){
                   ob[5]=no;
                   dmD.add(ob);
               }
            }
           if(tomaMno.isSelected()==true){
              ob[4]=no;
              if(hospSi.isSelected()==true){
                   ob[5]=hospita;
                   dmD.add(ob);
               }
               if(hospNo.isSelected()==true){
                   ob[5]=no;
                   dmD.add(ob);
               }
               
           }
        }
        
        if(checDental.isSelected()==true){
            ob[0]=idPaciente;
            ob[1]=Tdental;
            ob[2]=nom;
            ob[3]=cont;
           if(tomaMsi.isSelected()==true){  
               ob[4]=medica;
               if(hospSi.isSelected()==true){
                   ob[5]=hospita;
                   dmD.add(ob);
               }
               if(hospNo.isSelected()==true){
                   ob[5]=no;
                   dmD.add(ob);
               }
            }
           if(tomaMno.isSelected()==true){
              ob[4]=no;
              if(hospSi.isSelected()==true){
                   ob[5]=hospita; 
                   dmD.add(ob);
               }
               if(hospNo.isSelected()==true){
                   ob[5]=no;
                   dmD.add(ob);
               }
           }
        }
        } catch (Exception e) {JOptionPane.showMessageDialog(this,"Favor de Checar Campos"+e.getMessage());
        
        }
        
    }
